#While loop

count = 0
while (count < 3): 
    count = count + 1
    print("Hello World") 
print(".................................")
#Using else statement with While Loop 
count = 0
while (count < 3): 
    count = count + 1
    print("Hello Geek") 
else: 
    print("In Else Block") 
    
print(".................................")
    
#infinite loop 
count = 0
while (count == 0): 
    print("Hello Geek") 
    
print(".................................")

